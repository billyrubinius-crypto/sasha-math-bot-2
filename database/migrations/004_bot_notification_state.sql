-- Миграция 004 — персистентное состояние рассылки бота-ученика (main.py)
--
-- Зачем: main.py хранил "уже отправлено сегодня" только в памяти процесса
-- (last_morning_sent/last_evening_sent) — известное ограничение, зафиксированное в
-- ARCHITECTURE.md ("Уведомления", раздел "Известные ограничения"). Любой рестарт процесса
-- (передеплой, краш) после времени срабатывания (9:00/19:00 МСК) приводил к повторной отправке
-- того же дня. Стало реальной проблемой 2026-07-11: серия несвязанных пушей в приватный
-- репозиторий (Watch Paths не был настроен на сервисах Railway) вызвала 5 передеплоев main.py
-- за 45 минут, и утренняя рассылка ушла ученикам повторно 5 раз подряд.
--
-- Таблица маленькая и не растёт — по одной строке на тип уведомления ('morning_digest',
-- 'evening_reminder'). RLS не включён — весь остальной прод открыт тем же анонимным ключом
-- (принятый риск, ROADMAP.md T10), это не создаёт новой дыры сверх уже существующей.

create table if not exists public.bot_notification_state (
  notification_key  text  primary key,
  last_sent_date    date
);
