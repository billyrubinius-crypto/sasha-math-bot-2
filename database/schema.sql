-- Открытым может быть только один сезон одновременно.
create unique index if not exists idx_seasons_one_active
  on public.seasons ((true)) where end_date is null;

-- season_results — архив: итоговые очки и место ученика в закрытом сезоне.
-- Пишется один раз при закрытии сезона (G7), дальше только читается.
create table public.season_results (
  id          uuid         primary key default gen_random_uuid(),
  season_id   bigint       not null references public.seasons (id),
  student_id  bigint       not null references public.students (telegram_id),
  points      integer      not null,
  place       integer      not null,
  created_at  timestamptz  not null default now(),
  unique (season_id, student_id)
