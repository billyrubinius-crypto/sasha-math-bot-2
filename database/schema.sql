-- close_season — закрытие сезона одной транзакцией (миграция 006, переписана W09/017,
-- амоп ledger amount<>0 в 018, расширена лигами в 019/L01):
-- архив итогов в season_results с ДЕТЕРМИНИРОВАННЫМ tie-break (rating desc → меньше штрафов
-- в сезоне → раньше набрал очки по season_points_log → telegram_id), места тотальны
-- (row_number, без деления одного места). Призы топ-3 с ненулевыми очками (100/60/30) по
-- одному ученику на место — фиксированный фонд ≤ 190. Обнуление rating, закрытие сезона,
-- открытие следующего. Сезон, открытый сегодня, закрыть нельзя (защита от двойного клика).
-- L01: между глобальной выплатой и обнулением rating вызывается close_league_season
-- (места/переходы/Корона закрытого сезона + посев когорт нового); global top-3 не изменён.
CREATE OR REPLACE FUNCTION public.close_season()
 RETURNS json
 LANGUAGE plpgsql
AS $function$
declare
  v_season_id    bigint;
  v_new_season_id bigint;
  v_start_date   date;
  v_start_ts     timestamptz;
  v_today        date := (now() at time zone 'Europe/Moscow')::date;
  v_archived     integer;
  v_awarded      integer := 0;
  v_reward       integer;
  r record;
begin
  select id, start_date into v_season_id, v_start_date
    from seasons
    where end_date is null
    order by id desc
    limit 1
    for update;

  if v_season_id is null then
    raise exception 'Нет открытого сезона';
  end if;

  if v_start_date >= v_today then
    raise exception 'Сезон №% открыт сегодня — закрывать можно не раньше следующего дня', v_season_id;
  end if;

  v_start_ts := (v_start_date::timestamp) at time zone 'Europe/Moscow';

  -- Блокируем учеников до снимка очков (гонка с add_season_points, см. миграцию 006).
  perform 1 from students for update;

  insert into season_results (season_id, student_id, points, place)
  select v_season_id, s.telegram_id, s.rating,
         row_number() over (
           order by s.rating desc,
                    coalesce(pen.cnt, 0) asc,
                    pts.last_scored asc nulls last,
                    s.telegram_id asc)
    from students s
    left join (
      select student_id, count(*) as cnt
        from balance_history
       where reason like 'penalty:%' and created_at >= v_start_ts
       group by student_id) pen on pen.student_id = s.telegram_id
    left join (
      select student_id, max(created_at) as last_scored
        from season_points_log
       where season_id = v_season_id and amount <> 0
       group by student_id) pts on pts.student_id = s.telegram_id;
  get diagnostics v_archived = row_count;

  for r in
    select student_id, place
      from season_results
      where season_id = v_season_id and place <= 3 and points > 0
      order by place
  loop
    v_reward := case r.place when 1 then 100 when 2 then 60 else 30 end;
    perform add_huikons(r.student_id, v_reward, 'season_place_' || r.place);
    v_awarded := v_awarded + 1;
  end loop;

  -- Открываем следующий сезон заранее, чтобы короне и посеву когорт было куда встать (L01).
  update seasons set end_date = v_today where id = v_season_id;
  insert into seasons (start_date) values (v_today) returning id into v_new_season_id;

  -- Лиги: места/переходы/корона закрытого сезона + посев когорт нового (L01). Использует
  -- ещё не обнулённый season_results; для legacy-сезона без когорт — тихий no-op.
  perform public.close_league_season(v_season_id, v_new_season_id);

  update students set rating = 0 where rating <> 0;

  return json_build_object(
    'season_id', v_season_id,
    'archived', v_archived,
    'awarded', v_awarded
  );
end;
$function$;

-- buy_streak_shield — атомарная покупка щита (миграция 007, задача G9): лимит и цена через
-- add_huikons (reason='buy_streak_shield'), инкремент инвентаря. Мотивация атомарности и
-- блокировок — в шапке миграции 007. Цена 40→90 и лимит 2→7 подняты миграцией 012 (W04,
-- SPEC_STAGE2_5 §7.2 / ECONOMY_V2 §6) по отдельному решению пользователя: щит — часть
-- недельной модели, а не общий пересчёт магазина. Существующие щиты сохранены как есть.
CREATE OR REPLACE FUNCTION public.buy_streak_shield(p_student_id bigint)
 RETURNS json
 LANGUAGE plpgsql
AS $function$
declare
  v_price  integer := 90;
  v_max    integer := 7;
  v_qty    integer;
  v_balance integer;
  v_new_balance integer;
begin
  select quantity into v_qty
    from student_items
    where student_id = p_student_id and item_code = 'streak_shield'
    for update;
  if v_qty is null then v_qty := 0; end if;

  if v_qty >= v_max then
    raise exception 'Лимит: не больше % щитов в запасе', v_max;
  end if;

  select huikons into v_balance
    from students
    where telegram_id = p_student_id
    for update;
  if v_balance is null then
    raise exception 'Ученик % не найден', p_student_id;
  end if;
  if v_balance < v_price then
    raise exception 'Недостаточно бубликов: нужно %, есть %', v_price, v_balance;
  end if;

  select new_balance into v_new_balance
    from add_huikons(p_student_id, -v_price, 'buy_streak_shield');

  insert into student_items (student_id, item_code, quantity)
    values (p_student_id, 'streak_shield', 1)
    on conflict (student_id, item_code)
    do update set quantity = student_items.quantity + 1, updated_at = now();

  select quantity into v_qty
    from student_items
    where student_id = p_student_id and item_code = 'streak_shield';

  return json_build_object('quantity', v_qty, 'balance', v_new_balance);
end;
$function$;

-- consume_streak_shield — атомарно покрыть один пропущенный день щитом (миграция 007, G9).
-- Идемпотентно: true если день покрыт (сейчас или ранее), false если щита нет.
CREATE OR REPLACE FUNCTION public.consume_streak_shield(p_student_id bigint, p_bridged_date date)
 RETURNS boolean
 LANGUAGE plpgsql
AS $function$
declare
  v_qty integer;
begin
  if exists (select 1 from streak_shield_uses
               where student_id = p_student_id and bridged_date = p_bridged_date) then
    return true;
  end if;

  select quantity into v_qty
    from student_items
    where student_id = p_student_id and item_code = 'streak_shield'
    for update;

  if v_qty is null or v_qty <= 0 then
    return false;
  end if;

  insert into streak_shield_uses (student_id, bridged_date)
    values (p_student_id, p_bridged_date)
    on conflict (student_id, bridged_date) do nothing;

  update student_items
    set quantity = quantity - 1, updated_at = now()
    where student_id = p_student_id and item_code = 'streak_shield';

  return true;
end;
$function$;

-- ensure_season_rotation / buy_item / equip_item — RPC магазина (миграция 008, задача S1;
-- buy_item/equip_item точечно расширены миграцией 010 для персонального титула S8).
-- Тела и мотивация решений — в миграции 008 (ленивое назначение бандла открытому сезону;
-- единая покупка с делегированием щита, явной проверкой баланса, gate по достижению и
-- автоэкипировкой; бесплатное переодевание купленного). Сид каталога — тоже в миграции 008.
CREATE OR REPLACE FUNCTION public.ensure_season_rotation()
 RETURNS integer
 LANGUAGE plpgsql
AS $function$
declare
  v_season bigint;
  v_bundle integer;
begin
  select id into v_season from seasons where end_date is null order by id desc limit 1;
  if v_season is null then
    return null;
  end if;

  select bundle into v_bundle from season_bundles where season_id = v_season;
  if v_bundle is not null then
    return v_bundle;
  end if;

  select min(rotation_bundle) into v_bundle
    from shop_items
    where rotation_bundle is not null
      and active
      and rotation_bundle not in (select bundle from season_bundles);
  if v_bundle is null then
    return null;
  end if;

  insert into season_bundles (season_id, bundle)
    values (v_season, v_bundle)
    on conflict (season_id) do nothing;

  select bundle into v_bundle from season_bundles where season_id = v_season;
  return v_bundle;
end;
$function$;

-- submit_custom_title / review_custom_title — покупка права и модерация персонального
-- титула (миграция 010, S8). Первая отправка списывает цену через add_huikons; retry после
-- rejected бесплатен; только approved создаёт владение и публичную экипировку.
CREATE OR REPLACE FUNCTION public.submit_custom_title(p_student_id bigint, p_title_text text)
 RETURNS json
 LANGUAGE plpgsql
AS $function$
declare
  v_title       text;
  v_status      text;
  v_price       integer;
  v_balance     integer;
  v_new_balance integer;
begin
  if p_title_text is null or p_title_text ~ '[[:cntrl:]]' then
    raise exception 'Титул должен быть одной строкой без управляющих символов';
  end if;

  v_title := regexp_replace(btrim(p_title_text), '[[:space:]]+', ' ', 'g');
  if char_length(v_title) < 3 or char_length(v_title) > 24 then
    raise exception 'Длина титула должна быть от 3 до 24 символов';
  end if;

  select huikons into v_balance
    from public.students
    where telegram_id = p_student_id
    for update;
  if v_balance is null then
    raise exception 'Ученик % не найден', p_student_id;
  end if;

  select status into v_status
    from public.student_custom_titles
    where student_id = p_student_id
    for update;

  if v_status = 'pending' then
    raise exception 'Титул уже находится на модерации';
  elsif v_status = 'approved' then
    raise exception 'Персональный титул уже одобрен и не может быть изменён';
  elsif v_status = 'rejected' then
    update public.student_custom_titles
      set title_text = v_title,
          status = 'pending',
          teacher_comment = null,
          submitted_at = now(),
          reviewed_at = null,
          updated_at = now()
      where student_id = p_student_id;

    return json_build_object('status', 'pending', 'balance', v_balance, 'charged', 0);
  end if;

  select price into v_price
    from public.shop_items
    where item_code = 'title_custom' and active;
  if v_price is null then
    raise exception 'Персональный титул сейчас недоступен';
  end if;
  if v_balance < v_price then
    raise exception 'Недостаточно бубликов: нужно %, есть %', v_price, v_balance;
  end if;

  select new_balance into v_new_balance
    from public.add_huikons(p_student_id, -v_price, 'buy_title_custom');

  insert into public.student_custom_titles (student_id, title_text, status)
    values (p_student_id, v_title, 'pending');

  return json_build_object(
    'status', 'pending',
    'balance', v_new_balance,
    'charged', v_price
  );
end;
$function$;

CREATE OR REPLACE FUNCTION public.review_custom_title(
  p_student_id bigint,
  p_decision text,
  p_teacher_comment text DEFAULT null
)
 RETURNS json
 LANGUAGE plpgsql
AS $function$
declare
  v_title   text;
  v_status  text;
  v_comment text;
begin
  if p_decision is null or p_decision not in ('approved', 'rejected') then
    raise exception 'Решение должно быть approved или rejected';
  end if;

  select title_text, status into v_title, v_status
    from public.student_custom_titles
    where student_id = p_student_id
    for update;
  if v_status is null then
    raise exception 'Заявка ученика % не найдена', p_student_id;
  end if;
  if v_status <> 'pending' then
    raise exception 'Заявка уже рассмотрена: %', v_status;
  end if;

  if p_decision = 'rejected' then
    if p_teacher_comment is null or p_teacher_comment ~ '[[:cntrl:]]' then
      raise exception 'При отказе нужна причина одной строкой';
    end if;
    v_comment := regexp_replace(btrim(p_teacher_comment), '[[:space:]]+', ' ', 'g');
    if char_length(v_comment) < 3 or char_length(v_comment) > 200 then
      raise exception 'Причина отказа должна быть от 3 до 200 символов';
    end if;

    update public.student_custom_titles
      set status = 'rejected',
          teacher_comment = v_comment,
          reviewed_at = now(),
          updated_at = now()
      where student_id = p_student_id;

    return json_build_object('status', 'rejected');
  end if;

  update public.student_custom_titles
    set status = 'approved',
        teacher_comment = null,
        reviewed_at = now(),
        updated_at = now()
    where student_id = p_student_id;

  insert into public.student_items (student_id, item_code, quantity)
    values (p_student_id, 'title_custom', 1)
    on conflict (student_id, item_code)
    do update set quantity = greatest(student_items.quantity, 1), updated_at = now();

  insert into public.student_equipment (student_id, slot, item_code, variant)
    values (p_student_id, 'title', 'title_custom', v_title)
    on conflict (student_id, slot)
    do update set item_code = excluded.item_code,
                  variant = excluded.variant,
                  updated_at = now();

  return json_build_object('status', 'approved', 'title_text', v_title);
end;
$function$;

CREATE OR REPLACE FUNCTION public.buy_item(p_student_id bigint, p_item_code text, p_variant text DEFAULT null)
 RETURNS json
 LANGUAGE plpgsql
AS $function$
declare
  v_item     shop_items%rowtype;
  v_bundle   integer;
  v_balance  integer;
  v_new_balance integer;
begin
  select * into v_item from shop_items where item_code = p_item_code and active;
  if v_item.item_code is null then
    raise exception 'Товар % не найден или снят с продажи', p_item_code;
  end if;

  if p_item_code = 'title_custom' then
    raise exception 'Персональный титул покупается только через отправку на модерацию';
  end if;

  if v_item.item_kind = 'shield' then
    return buy_streak_shield(p_student_id);
  end if;

  if v_item.availability = 'rotation' then
    v_bundle := ensure_season_rotation();
    if v_bundle is null or v_bundle <> v_item.rotation_bundle then
      raise exception 'Товар «%» сейчас не на витрине', v_item.name;
    end if;
  end if;

  if v_item.condition_achievement is not null then
    if not exists (select 1 from student_achievements
                     where student_id = p_student_id
                       and achievement_code = v_item.condition_achievement) then
      raise exception 'Для покупки «%» нужно достижение', v_item.name;
    end if;
  end if;

  if v_item.item_kind = 'service' then
    if p_variant is null or position(p_variant in coalesce(v_item.render_payload, '')) = 0 then
      raise exception 'Недопустимый вариант для «%»', v_item.name;
    end if;
  end if;

  if v_item.item_kind = 'cosmetic' then
    if exists (select 1 from student_items
                 where student_id = p_student_id and item_code = p_item_code) then
      raise exception 'Уже куплено';
    end if;
  end if;

  select huikons into v_balance from students where telegram_id = p_student_id for update;
  if v_balance is null then
    raise exception 'Ученик % не найден', p_student_id;
  end if;
  if v_balance < v_item.price then
    raise exception 'Недостаточно бубликов: нужно %, есть %', v_item.price, v_balance;
  end if;

  select new_balance into v_new_balance
    from add_huikons(p_student_id, -v_item.price, 'buy_' || p_item_code);

  if v_item.item_kind = 'cosmetic' then
    insert into student_items (student_id, item_code, quantity)
      values (p_student_id, p_item_code, 1);
    insert into student_equipment (student_id, slot, item_code)
      values (p_student_id, v_item.slot, p_item_code)
      on conflict (student_id, slot)
      do update set item_code = excluded.item_code, variant = null, updated_at = now();
  elsif v_item.item_kind = 'service' then
    insert into student_equipment (student_id, slot, item_code, variant)
      values (p_student_id, v_item.slot, p_item_code, p_variant)
      on conflict (student_id, slot)
      do update set item_code = excluded.item_code, variant = excluded.variant, updated_at = now();
  end if;

  return json_build_object('item_code', p_item_code, 'balance', v_new_balance);
end;
$function$;

CREATE OR REPLACE FUNCTION public.equip_item(p_student_id bigint, p_slot text, p_item_code text DEFAULT null)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
declare
  v_slot         text;
  v_custom_title text;
begin
  if p_item_code is null then
    delete from student_equipment where student_id = p_student_id and slot = p_slot;
    return;
  end if;

  if p_slot = 'status_emoji' then
    raise exception 'Эмодзи-статус меняется только покупкой смены';
  end if;

  select slot into v_slot from shop_items where item_code = p_item_code and active;
  if v_slot is null or v_slot <> p_slot then
    raise exception 'Товар % не подходит слоту %', p_item_code, p_slot;
  end if;

  if not exists (select 1 from student_items
                   where student_id = p_student_id and item_code = p_item_code) then
    raise exception 'Сначала нужно купить этот предмет';
  end if;

  if p_item_code = 'title_custom' then
    select title_text into v_custom_title
      from student_custom_titles
      where student_id = p_student_id and status = 'approved';
    if v_custom_title is null then
      raise exception 'Персональный титул ещё не одобрен';
    end if;

    insert into student_equipment (student_id, slot, item_code, variant)
      values (p_student_id, p_slot, p_item_code, v_custom_title)
      on conflict (student_id, slot)
      do update set item_code = excluded.item_code,
                    variant = excluded.variant,
                    updated_at = now();
    return;
  end if;

  insert into student_equipment (student_id, slot, item_code)
    values (p_student_id, p_slot, p_item_code)
    on conflict (student_id, slot)
    do update set item_code = excluded.item_code, variant = null, updated_at = now();
end;
$function$;

-- set_showcase — поставить/снять предмет витрины профиля (миграция 009, S7). p_ref_code=null
-- снимает слот; иначе проверяет владение (student_items) или получение (student_achievements)
-- в зависимости от p_kind перед апсертом. Мотивация отдельной таблицы — в шапке миграции 009.
CREATE OR REPLACE FUNCTION public.set_showcase(p_student_id bigint, p_position smallint, p_kind text, p_ref_code text)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
begin
  if p_ref_code is null then
    delete from student_showcase where student_id = p_student_id and position = p_position;
    return;
  end if;

  if p_kind = 'item' then
    if not exists (select 1 from student_items where student_id = p_student_id and item_code = p_ref_code) then
      raise exception 'Предмет % не куплен', p_ref_code;
    end if;
  elsif p_kind = 'achievement' then
    if not exists (select 1 from student_achievements where student_id = p_student_id and achievement_code = p_ref_code) then
      raise exception 'Достижение % не получено', p_ref_code;
    end if;
  else
    raise exception 'Неизвестный тип витрины: %', p_kind;
  end if;

  insert into student_showcase (student_id, position, kind, ref_code)
    values (p_student_id, p_position, p_kind, p_ref_code)
    on conflict (student_id, position)
    do update set kind = excluded.kind, ref_code = excluded.ref_code, updated_at = now();
end;
$function$;

-- add_season_points — атомарное начисление очков сезона (миграция 005, задача G3).
-- PostgREST не умеет относительный update, поэтому rating = rating + N делается здесь.
-- Очки сезона — отдельная валюта от бубликов: в balance_history НЕ пишет.
CREATE OR REPLACE FUNCTION public.add_season_points(p_student_id bigint, p_amount integer)
 RETURNS integer
 LANGUAGE plpgsql
AS $function$
declare
  v_new integer;
begin
  update students
    set rating = greatest(0, rating + p_amount)
    where telegram_id = p_student_id
    returning rating into v_new;

  if v_new is null then
    raise exception 'Student % not found', p_student_id;
  end if;

  return v_new;
end;
$function$;

-- sync_student_week_assignments — единственный алгоритм материализации недельных
-- планов в персональные assignments (миграция 011, W01; SPEC_STAGE2_5.md §5).
-- Вызывается publish_weekly_plan / cancel_weekly_plan и триггером students.
-- p_replace_from — с какой scheduled_date можно заменять/удалять НЕНАЧАТЫЕ
-- daily-строки: публикация/отмена передают завтра («только будущие», §5.2),
-- смена группы/новый ученик — сегодня (§5.3–5.4; null = сегодня). Вставка
-- отсутствующих строк — всегда от сегодня. weekly меняется, пока строка не
-- начата, без гейта по дате (§5.5). Полная мотивация — в шапке миграции 011.
CREATE OR REPLACE FUNCTION public.sync_student_week_assignments(
  p_student_id   bigint,
  p_week_start   date,
  p_replace_from date DEFAULT null
)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
declare
  v_today        date := (now() at time zone 'Europe/Moscow')::date;
  v_week_label   text;
  v_insert_from  date;
  v_replace_from date;
  v_group        text;
  v_eff_ids      uuid[] := '{}';
  v_slot_date    date;
  v_existing     public.assignments%rowtype;
  v_started      boolean;
  r              record;
begin
  if p_week_start is null or extract(isodow from p_week_start) <> 1 then
    raise exception 'week_start % — не понедельник', p_week_start;
  end if;

  -- Прошедшие недели не материализуются и не переписываются (SPEC §5.3).
  if p_week_start + 6 < v_today then
    return;
  end if;

  select group_name into v_group
    from public.students
    where telegram_id = p_student_id;
  if not found then
    raise exception 'Ученик % не найден', p_student_id;
  end if;

  v_week_label   := to_char(p_week_start, 'YYYY-MM-DD');
  v_insert_from  := greatest(p_week_start, v_today);
  v_replace_from := greatest(p_week_start, coalesce(p_replace_from, v_today));

  -- Эффективный item на каждый слот: item точной группы ученика,
  -- иначе item плана all, иначе слота нет (SPEC §5.1, приоритет по слоту).
  for r in
    with candidate_items as (
      select i.id, i.type, i.day_of_week, i.title, i.content_url,
             i.teacher_comment, i.task_count, p.audience_type, p.group_name
        from public.weekly_plan_items i
        join public.weekly_plans p on p.id = i.plan_id
       where p.week_start = p_week_start
         and p.status = 'published'
         and i.active
         and (p.audience_type = 'all'
              or (v_group is not null
                  and p.audience_type = 'group'
                  and p.group_name = v_group))
    )
    select distinct on (c.type, coalesce(c.day_of_week, 0))
           c.id as item_id, c.type, c.day_of_week, c.title, c.content_url,
           c.teacher_comment, c.task_count,
           case when c.audience_type = 'group' then c.group_name
                else 'Все ученики' end as aud_label
      from candidate_items c
     order by c.type, coalesce(c.day_of_week, 0),
              (c.audience_type = 'group') desc
  loop
    v_eff_ids := v_eff_ids || r.item_id;

    if r.type = 'daily' then
      v_slot_date := p_week_start + (r.day_of_week - 1);
    else
      v_slot_date := p_week_start;
    end if;

    -- Существующая плановая строка слота (максимум одна — частичные уникальности).
    select a.* into v_existing
      from public.assignments a
     where a.student_id = p_student_id
       and a.plan_item_id is not null
       and ((r.type = 'daily'  and a.type = 'daily'  and a.scheduled_date = v_slot_date)
         or (r.type = 'weekly' and a.type = 'weekly' and a.week_label = v_week_label))
     limit 1
     for update;

    if found then
      v_started := v_existing.status is distinct from 'assigned'
                or v_existing.submitted_at is not null
                or v_existing.photo_url is not null
                or v_existing.first_submitted_at is not null;

      if v_started then
        continue;  -- начатая история неприкосновенна (SPEC §5.4)
      end if;

      if r.type = 'daily' and v_slot_date < v_replace_from then
        continue;  -- замена daily разрешена только с v_replace_from
      end if;

      if v_existing.plan_item_id       is distinct from r.item_id
         or v_existing.title           is distinct from r.title
         or v_existing.content_url     is distinct from r.content_url
         or v_existing.teacher_comment is distinct from r.teacher_comment
         or v_existing.task_count      is distinct from r.task_count
         or v_existing.assigned_group  is distinct from r.aud_label then
        update public.assignments
           set plan_item_id    = r.item_id,
               title           = r.title,
               content_url     = r.content_url,
               teacher_comment = r.teacher_comment,
               task_count      = r.task_count,
               assigned_group  = r.aud_label
         where id = v_existing.id;
      end if;
    else
      if r.type = 'daily' and v_slot_date < v_insert_from then
        continue;  -- прошлые дни не создаются и не входят в N (SPEC §5.3)
      end if;

      insert into public.assignments
        (student_id, type, title, content_url, teacher_comment, day_of_week,
         week_label, scheduled_date, activation_status, status, assigned_group,
         plan_item_id, task_count)
      values
        (p_student_id, r.type, r.title, r.content_url, r.teacher_comment,
         r.day_of_week, v_week_label, v_slot_date, 'scheduled', 'assigned',
         r.aud_label, r.item_id, r.task_count)
      on conflict (student_id, plan_item_id) where plan_item_id is not null
      do nothing;  -- гонка параллельных синхронизаций не даёт второй строки
    end if;
  end loop;

  -- Неначатые плановые строки недели без эффективного item (item деактивирован,
  -- план отменён, в новой аудитории слота нет) удаляются: daily — не раньше
  -- v_replace_from, weekly — пока не начата (SPEC §5.4–5.5). Legacy-строки
  -- (plan_item_id = null) не затрагиваются.
  delete from public.assignments a
   where a.student_id = p_student_id
     and a.plan_item_id is not null
     and a.week_label = v_week_label
     and a.status = 'assigned'
     and a.submitted_at is null
     and a.photo_url is null
     and a.first_submitted_at is null
     and (a.type <> 'daily'
          or (a.scheduled_date is not null and a.scheduled_date >= v_replace_from))
     and not (a.plan_item_id = any (v_eff_ids));
end;
$function$;

-- publish_weekly_plan — публикация И редактирование плана (upsert item'ов по
-- слотам) одной транзакцией с синхронизацией затронутых учеников (миграция 011;
-- SPEC §5.2, §5.5). Мультивыбор групп в UI = отдельный вызов на каждую группу.
-- Слоты, отсутствующие в p_items, деактивируются (кроме замороженных daily
-- текущей недели, чей день уже наступил). Повторный вызов с теми же данными
-- ничего не меняет. Полная мотивация — в шапке миграции 011.
CREATE OR REPLACE FUNCTION public.publish_weekly_plan(
  p_week_start    date,
  p_audience_type text,
  p_group_name    text DEFAULT null,
  p_items         jsonb DEFAULT '[]'::jsonb
)
 RETURNS json
 LANGUAGE plpgsql
AS $function$
declare
  v_today        date := (now() at time zone 'Europe/Moscow')::date;
  v_plan         public.weekly_plans%rowtype;
  v_item         public.weekly_plan_items%rowtype;
  itm            jsonb;
  v_type         text;
  v_dow          integer;
  v_title        text;
  v_url          text;
  v_comment      text;
  v_task_count   integer;
  v_slot_key     text;
  v_slot_date    date;
  v_touched      text[] := '{}';
  v_replace_from date;
  v_synced       integer := 0;
  v_active_items integer;
  r              record;
begin
  if p_week_start is null or extract(isodow from p_week_start) <> 1 then
    raise exception 'week_start должен быть понедельником';
  end if;
  if p_week_start + 6 < v_today then
    raise exception 'Неделя % уже прошла — публикация и редактирование запрещены', p_week_start;
  end if;
  if p_audience_type is null or p_audience_type not in ('all', 'group') then
    raise exception 'audience_type должен быть all или group';
  end if;
  if p_audience_type = 'group' and (p_group_name is null or btrim(p_group_name) = '') then
    raise exception 'Для группового плана нужна непустая группа';
  end if;
  if p_audience_type = 'all' and p_group_name is not null then
    raise exception 'Для плана all группа не указывается';
  end if;
  if p_items is null or jsonb_typeof(p_items) <> 'array' then
    raise exception 'items должен быть json-массивом';
  end if;

  -- Активный план аудитории; отменённые остаются историей — создаётся новый.
  select * into v_plan
    from public.weekly_plans
   where week_start = p_week_start
     and audience_type = p_audience_type
     and group_name is not distinct from p_group_name
     and status = 'published'
   for update;

  if not found then
    insert into public.weekly_plans (week_start, audience_type, group_name, status)
      values (p_week_start, p_audience_type, p_group_name, 'published')
      returning * into v_plan;
  end if;

  -- Upsert item'ов по слотам.
  for itm in select value from jsonb_array_elements(p_items)
  loop
    v_type := itm->>'type';
    if v_type is null or v_type not in ('daily', 'weekly') then
      raise exception 'Тип задания должен быть daily или weekly';
    end if;

    if v_type = 'daily' then
      v_dow := (itm->>'day_of_week')::integer;
      if v_dow is null or v_dow < 1 or v_dow > 7 then
        raise exception 'day_of_week для daily должен быть 1–7';
      end if;
      v_slot_key  := 'daily-' || v_dow;
      v_slot_date := p_week_start + (v_dow - 1);
    else
      if itm->>'day_of_week' is not null then
        raise exception 'day_of_week для weekly не указывается';
      end if;
      v_dow := null;
      v_slot_key  := 'weekly';
      v_slot_date := p_week_start;
    end if;

    if v_slot_key = any (v_touched) then
      raise exception 'Слот % указан в items дважды', v_slot_key;
    end if;
    v_touched := v_touched || v_slot_key;

    v_title      := btrim(coalesce(itm->>'title', ''));
    v_url        := btrim(coalesce(itm->>'content_url', ''));
    v_comment    := nullif(btrim(coalesce(itm->>'teacher_comment', '')), '');
    v_task_count := (itm->>'task_count')::integer;
    if v_title = '' or v_url = '' then
      raise exception 'У задания слота % должны быть название и ссылка', v_slot_key;
    end if;
    if v_task_count is not null and v_task_count <= 0 then
      raise exception 'task_count должен быть положительным числом или null';
    end if;

    select * into v_item
      from public.weekly_plan_items
     where plan_id = v_plan.id
       and active
       and type = v_type
       and day_of_week is not distinct from v_dow
     for update;

    if found then
      if v_item.title = v_title
         and v_item.content_url = v_url
         and v_item.teacher_comment is not distinct from v_comment
         and v_item.task_count is not distinct from v_task_count then
        continue;  -- идемпотентный повтор публикации
      end if;
      -- Редактирование текущей daily после наступления дня запрещено (SPEC §5.5).
      if v_type = 'daily' and v_slot_date <= v_today then
        raise exception 'День % уже наступил — редактирование этого daily запрещено', v_slot_date;
      end if;
      update public.weekly_plan_items
         set title           = v_title,
             content_url     = v_url,
             teacher_comment = v_comment,
             task_count      = v_task_count,
             updated_at      = now()
       where id = v_item.id;
    else
      -- Новый item на прошедший день не добавляется; на сегодня — разрешён
      -- (согласовано с SPEC §5.3: новичок получает daily от today).
      if v_type = 'daily' and v_slot_date < v_today then
        raise exception 'День % уже прошёл — добавить daily задним числом нельзя', v_slot_date;
      end if;
      insert into public.weekly_plan_items
        (plan_id, type, day_of_week, title, content_url, teacher_comment, task_count)
      values
        (v_plan.id, v_type, v_dow, v_title, v_url, v_comment, v_task_count);
    end if;
  end loop;

  -- Слоты, отсутствующие в новой публикации: active = false (SPEC §5.5).
  -- Замороженные daily (день наступил) молча сохраняются.
  for r in
    select i.id, i.type,
           case when i.type = 'daily' then p_week_start + (i.day_of_week - 1)
                else p_week_start end as slot_date,
           case when i.type = 'daily' then 'daily-' || i.day_of_week
                else 'weekly' end as slot_key
      from public.weekly_plan_items i
     where i.plan_id = v_plan.id
       and i.active
  loop
    if r.slot_key = any (v_touched) then
      continue;
    end if;
    if r.type = 'daily' and r.slot_date <= v_today then
      continue;
    end if;
    update public.weekly_plan_items
       set active = false, updated_at = now()
     where id = r.id;
  end loop;

  update public.weekly_plans set updated_at = now() where id = v_plan.id;

  -- Синхронизация затронутых учеников той же транзакцией (SPEC §5.2):
  -- замена/удаление только будущих неначатых daily-строк (с завтра).
  v_replace_from := greatest(p_week_start, v_today + 1);
  for r in
    select telegram_id
      from public.students
     where p_audience_type = 'all' or group_name = p_group_name
  loop
    perform public.sync_student_week_assignments(r.telegram_id, p_week_start, v_replace_from);
    v_synced := v_synced + 1;
  end loop;

  select count(*) into v_active_items
    from public.weekly_plan_items
   where plan_id = v_plan.id and active;

  return json_build_object(
    'plan_id',         v_plan.id,
    'week_start',      v_plan.week_start,
    'audience_type',   v_plan.audience_type,
    'group_name',      v_plan.group_name,
    'active_items',    v_active_items,
    'students_synced', v_synced
  );
end;
$function$;

-- cancel_weekly_plan — отмена плана без удаления истории (миграция 011; SPEC §5.5):
-- начатые строки не трогаются; неначатые будущие пересобираются на fallback
-- (план all) либо удаляются. Повторная отмена идемпотентна.
CREATE OR REPLACE FUNCTION public.cancel_weekly_plan(p_plan_id uuid)
 RETURNS json
 LANGUAGE plpgsql
AS $function$
declare
  v_today        date := (now() at time zone 'Europe/Moscow')::date;
  v_plan         public.weekly_plans%rowtype;
  v_replace_from date;
  v_synced       integer := 0;
  r              record;
begin
  select * into v_plan
    from public.weekly_plans
    where id = p_plan_id
    for update;
  if not found then
    raise exception 'План % не найден', p_plan_id;
  end if;
  if v_plan.status = 'cancelled' then
    return json_build_object('plan_id', v_plan.id, 'status', 'cancelled', 'students_synced', 0);
  end if;
  if v_plan.week_start + 6 < v_today then
    raise exception 'Неделя % уже прошла — отменять её план нельзя', v_plan.week_start;
  end if;

  update public.weekly_plans
     set status = 'cancelled', updated_at = now()
   where id = v_plan.id;

  v_replace_from := greatest(v_plan.week_start, v_today + 1);
  for r in
    select telegram_id
      from public.students
     where v_plan.audience_type = 'all' or group_name = v_plan.group_name
  loop
    perform public.sync_student_week_assignments(r.telegram_id, v_plan.week_start, v_replace_from);
    v_synced := v_synced + 1;
  end loop;

  return json_build_object('plan_id', v_plan.id, 'status', 'cancelled', 'students_synced', v_synced);
end;
$function$;

-- --- Недельный расчёт: утилиты и RPC (миграция 012, W04) -----------------------
-- Полные решения и мотивация — в шапке миграции 012. Ключевые:
--   * поздняя ПЕРВАЯ сдача не засчитывается в A (решение пользователя 2026-07-16, SPEC §6.1);
--   * submitted_at/checked_at нормализуются серверным now() в триггере — клиентские часы не
--     определяют право на 24-часовое окно (SPEC §6.2);
--   * backfill legacy не делается: там, где нужна первая отправка, читается
--     coalesce(first_submitted_at, submitted_at);
--   * награда не начисляется: reward_amount сохраняется, add_huikons зовёт W09.

-- Понедельник учебной недели, содержащей дату (SPEC §3).
CREATE OR REPLACE FUNCTION public.week_start_of(p_date date)
 RETURNS date
 LANGUAGE sql
 IMMUTABLE
AS $function$
  select p_date - (extract(isodow from p_date)::int - 1);
$function$;

-- Момент следующего понедельника 00:00 МСК — правая граница учебной недели (SPEC §3).
CREATE OR REPLACE FUNCTION public.next_monday_msk(p_date date)
 RETURNS timestamptz
 LANGUAGE sql
 IMMUTABLE
AS $function$
  select ((public.week_start_of(p_date) + 7)::timestamp) at time zone 'Europe/Moscow';
$function$;

-- Недельная награда по эффективному результату E (SPEC §7.3, ECONOMY_V2 §4).
CREATE OR REPLACE FUNCTION public.weekly_reward_amount(p_effective integer)
 RETURNS integer
 LANGUAGE sql
 IMMUTABLE
AS $function$
  select case
    when p_effective >= 7 then 110
    when p_effective = 6  then 80
    when p_effective = 5  then 55
    when p_effective = 4  then 30
    else 0
  end;
$function$;

-- Была ли ПЕРВАЯ отправка своевременной (в свой scheduled_date по МСК).
CREATE OR REPLACE FUNCTION public.is_first_submission_on_time(
  p_first_submitted_at timestamptz,
  p_submitted_at       timestamptz,
  p_scheduled_date     date
)
 RETURNS boolean
 LANGUAGE sql
 IMMUTABLE
AS $function$
  select coalesce(p_first_submitted_at, p_submitted_at) is not null
     and p_scheduled_date is not null
     and (coalesce(p_first_submitted_at, p_submitted_at) at time zone 'Europe/Moscow')::date
         = p_scheduled_date;
$function$;

-- Доступный запас щитов = инвентарь минус активные резервы всех нефинализированных недель:
-- одну единицу нельзя обещать двум неделям (SPEC §7.2).
CREATE OR REPLACE FUNCTION public.available_shield_quantity(p_student_id bigint)
 RETURNS integer
 LANGUAGE sql
 STABLE
AS $function$
  select coalesce((select quantity from public.student_items
                    where student_id = p_student_id and item_code = 'streak_shield'), 0)
       - (select count(*) from public.weekly_shield_uses
           where student_id = p_student_id and status = 'requested')::int;
$function$;

-- request_weekly_shield / cancel_weekly_shield — ручной выбор щитов до финализации.
-- Ничего не списывают: списание только при финализации. Тела — в миграции 012.
CREATE OR REPLACE FUNCTION public.request_weekly_shield(p_student_id bigint, p_assignment_id uuid)
 RETURNS json
 LANGUAGE plpgsql
AS $function$
declare
  v_asn        public.assignments%rowtype;
  v_week_start date;
  v_status     text;
  v_available  integer;
begin
  perform 1 from public.student_items
    where student_id = p_student_id and item_code = 'streak_shield' for update;

  select * into v_asn from public.assignments where id = p_assignment_id;
  if not found or v_asn.student_id is distinct from p_student_id then
    raise exception 'Задание не найдено у этого ученика';
  end if;
  if v_asn.type <> 'daily' then
    raise exception 'Щит применяется только к ежедневкам';
  end if;
  if v_asn.scheduled_date is null then
    raise exception 'У задания нет даты — щит применить нельзя';
  end if;
  if v_asn.status = 'checked' and v_asn.approval_status = 'approved' then
    raise exception 'Работа уже принята — щит не нужен';
  end if;

  v_week_start := public.week_start_of(v_asn.scheduled_date);

  select status into v_status from public.student_week_results
    where student_id = p_student_id and week_start = v_week_start for update;
  if v_status in ('finalized', 'neutral') then
    raise exception 'Неделя уже закрыта — выбор щитов изменить нельзя';
  end if;

  if exists (select 1 from public.weekly_shield_uses
               where assignment_id = p_assignment_id and status = 'requested') then
    return json_build_object('status', 'requested', 'available', public.available_shield_quantity(p_student_id));
  end if;
  if exists (select 1 from public.weekly_shield_uses
               where assignment_id = p_assignment_id and status = 'consumed') then
    raise exception 'На это задание щит уже списан';
  end if;

  v_available := public.available_shield_quantity(p_student_id);
  if v_available < 1 then
    raise exception 'Нет свободных щитов: все либо не куплены, либо уже обещаны другой неделе';
  end if;

  insert into public.weekly_shield_uses (student_id, week_start, assignment_id, status)
    values (p_student_id, v_week_start, p_assignment_id, 'requested');

  perform public.recalc_student_week(p_student_id, v_week_start);

  return json_build_object('status', 'requested', 'available', public.available_shield_quantity(p_student_id));
end;
$function$;

CREATE OR REPLACE FUNCTION public.cancel_weekly_shield(p_student_id bigint, p_assignment_id uuid)
 RETURNS json
