  student_id  bigint       not null references public.students (telegram_id),
  position    smallint     not null check (position between 1 and 3),
  kind        text         not null check (kind in ('item', 'achievement')),
  ref_code    text         not null,
  updated_at  timestamptz  not null default now(),
  created_at  timestamptz  not null default now(),
  unique (student_id, position)
);
alter table public.student_showcase disable row level security;

-- --- Недельные планы Stage 2.5 (миграция 011, W01) ----------------------------
-- Шаблон плана недели, отделённый от персонального прогресса. assignments
-- остаётся персональным фактом задания и владельцем статуса сдачи; строки
-- материализуются серверной sync_student_week_assignments (см. функции ниже).
-- Полные решения и мотивация — в шапке миграции 011.

-- weekly_plans — одна строка = один план одной аудитории на одну неделю.
create table public.weekly_plans (
  id            uuid         primary key default gen_random_uuid(),
  week_start    date         not null check (extract(isodow from week_start) = 1),
  audience_type text         not null check (audience_type in ('all', 'group')),
  group_name    text,
  status        text         not null default 'published'
                             check (status in ('published', 'cancelled')),
  created_at    timestamptz  not null default now(),
  updated_at    timestamptz  not null default now(),
  check ((audience_type = 'all'   and group_name is null)
      or (audience_type = 'group' and group_name is not null and btrim(group_name) <> ''))
);

-- Одна АКТИВНАЯ строка на (week_start, audience_type, group_name);
-- отменённые планы — история, переизданию не мешают.
create unique index if not exists uq_weekly_plans_active_all
  on public.weekly_plans (week_start)
  where status = 'published' and audience_type = 'all';
create unique index if not exists uq_weekly_plans_active_group
  on public.weekly_plans (week_start, group_name)
  where status = 'published' and audience_type = 'group';

alter table public.weekly_plans disable row level security;

-- weekly_plan_items — слот плана: один daily на номер дня и один weekly.
-- Удалённый из плана item получает active = false, физически не уничтожается.
create table public.weekly_plan_items (
  id              uuid         primary key default gen_random_uuid(),
  plan_id         uuid         not null references public.weekly_plans (id),
  type            text         not null check (type in ('daily', 'weekly')),
  day_of_week     integer,
  title           text         not null check (btrim(title) <> ''),
  content_url     text         not null check (btrim(content_url) <> ''),
  teacher_comment text,
  task_count      integer      check (task_count is null or task_count > 0),
  active          boolean      not null default true,
  created_at      timestamptz  not null default now(),
  updated_at      timestamptz  not null default now(),
  check ((type = 'daily'  and day_of_week between 1 and 7)
      or (type = 'weekly' and day_of_week is null))
);

create unique index if not exists uq_weekly_plan_items_daily_slot
  on public.weekly_plan_items (plan_id, day_of_week)
  where active and type = 'daily';
create unique index if not exists uq_weekly_plan_items_weekly_slot
  on public.weekly_plan_items (plan_id)
  where active and type = 'weekly';

alter table public.weekly_plan_items disable row level security;

-- FK, отложенный из create table assignments (weekly_plan_items создаётся позже).
alter table public.assignments
  add constraint assignments_plan_item_id_fkey
  foreign key (plan_item_id) references public.weekly_plan_items (id);

-- Частичные уникальности плановых строк assignments (миграция 011):
-- legacy-строки (plan_item_id = null) под них не попадают.
create unique index if not exists uq_assignments_plan_daily
  on public.assignments (student_id, scheduled_date)
  where plan_item_id is not null and type = 'daily';
create unique index if not exists uq_assignments_plan_weekly
  on public.assignments (student_id, week_label)
  where plan_item_id is not null and type = 'weekly';
create unique index if not exists uq_assignments_plan_item
  on public.assignments (student_id, plan_item_id)
  where plan_item_id is not null;
create index if not exists idx_assignments_plan_item
  on public.assignments (plan_item_id)
  where plan_item_id is not null;

-- --- Недельный результат и щиты недели (миграция 012, W04) ---------------------
-- Серверный источник истины недельного итога. Награды здесь НЕ начисляются: finalize считает
-- и сохраняет reward_amount, выплату через add_huikons включает cutover W09. Расписание
-- (pg_cron) тоже включает W09 — 012 только создаёт finalize_due_student_weeks().
-- Полные решения и мотивация — в шапке миграции 012.

-- student_week_results — снимок недельного расчёта: N/A/S/E, статус и итог (SPEC §7.1).
-- Источником реальной работы остаются assignments; строка пересчитывается recalc_student_week
-- до финализации, после — заморожена.
create table public.student_week_results (
  id                    uuid         primary key default gen_random_uuid(),
  student_id            bigint       not null references public.students (telegram_id),
  week_start            date         not null check (extract(isodow from week_start) = 1),
  available_daily_count integer      not null default 0 check (available_daily_count >= 0),  -- N
  approved_daily_count  integer      not null default 0 check (approved_daily_count >= 0),   -- A
  requested_shields     integer      not null default 0 check (requested_shields >= 0),
  shields_used          integer      not null default 0 check (shields_used >= 0),           -- S
  effective_daily_count integer      not null default 0 check (effective_daily_count >= 0),  -- E
  status                text         not null default 'open'
                                     check (status in ('open', 'pending_review', 'awaiting_student', 'finalized', 'neutral')),
  successful            boolean,     -- null у нейтральной недели: она не успешная и не слабая
  reward_amount         integer      check (reward_amount is null or reward_amount >= 0),
  neutral_reason        text,
  finalized_at          timestamptz,
  created_at            timestamptz  not null default now(),
  updated_at            timestamptz  not null default now(),
  unique (student_id, week_start)
);

create index if not exists idx_student_week_results_open
  on public.student_week_results (week_start)
  where status not in ('finalized', 'neutral');

alter table public.student_week_results disable row level security;

-- weekly_shield_uses — резерв щита на КОНКРЕТНУЮ ежедневку (SPEC §7.2). Отдельная таблица от
-- streak_shield_uses (007): та закрывает разрывы старого processStreak(), эта резервирует щит
-- под недельный результат. on delete cascade — потому что sync_student_week_assignments (011)
-- удаляет неначатые плановые строки, а резерв ставится именно на неначатую ежедневку.
create table public.weekly_shield_uses (
  id            uuid         primary key default gen_random_uuid(),
  student_id    bigint       not null references public.students (telegram_id),
  week_start    date         not null check (extract(isodow from week_start) = 1),
  assignment_id uuid         not null references public.assignments (id) on delete cascade,
  status        text         not null check (status in ('requested', 'consumed', 'cancelled')),
  requested_at  timestamptz  not null default now(),
  consumed_at   timestamptz,
  cancelled_at  timestamptz,
  created_at    timestamptz  not null default now(),
  updated_at    timestamptz  not null default now()
);

-- Активный резерв уникален для assignment; списанный — тоже (нельзя списать дважды).
create unique index if not exists uq_weekly_shield_requested
  on public.weekly_shield_uses (assignment_id) where status = 'requested';
create unique index if not exists uq_weekly_shield_consumed
  on public.weekly_shield_uses (assignment_id) where status = 'consumed';
create index if not exists idx_weekly_shield_student_week
  on public.weekly_shield_uses (student_id, week_start);

alter table public.weekly_shield_uses disable row level security;

-- --- Еженедельные пробники Stage 2.5 (миграция 016, P02A) ----------------------
-- Канонический результат недельного пробника (score integer 0-100) отдельно от legacy
-- mock_exam_results (score text не трогается). Награда идемпотентна: бублики pay-once через
-- явный ledger, season points — детерминированная компенсирующая дельта. Запись только через
-- RPC record_weekly_mock_exam (см. функции ниже). Полная мотивация — в шапке миграции 016.
create table public.weekly_mock_exams (
