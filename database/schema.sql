-- --- Таблицы -----------------------------------------------------------------

-- students — родительская таблица для всех внешних ключей.
-- PK = id, но ссылки и вся бизнес-логика идут по telegram_id (см. индекс ниже).
create table public.students (
  id                        bigint  generated by default as identity primary key,
  telegram_id               bigint  not null,
  name                      text,
  rating                    integer default 50,
  huikons                   integer default 0,
  lives                     integer default 3,
  current_streak            integer default 0,
  last_submission_date_msk  date,
  telegram_username         text,
  group_name                text,
  payment_date              date            -- обнулена и не используется (миграция 002)
);

-- Уникальность telegram_id; на этот индекс ссылаются все FK ниже.
create unique index if not exists idx_students_telegram_id
  on public.students (telegram_id);

-- assignments — задание-строка на ученика (ежедневные/еженедельные/индивидуальные).
create table public.assignments (
  id                 uuid         primary key default gen_random_uuid(),
  student_id         bigint       references public.students (telegram_id),
  type               text         not null,
  title              text,
  content_url        text,
  teacher_comment    text,
  day_of_week        integer,
  week_label         text,
  scheduled_date     date,
  activation_status  text         default 'draft',
  status             text         default 'assigned',
  created_at         timestamptz  default now(),
  submitted_at       timestamptz,
  checked_at         timestamptz,
  photo_url          text,
  teacher_feedback   text,
  approval_status    text,
  assigned_group     text
);

-- balance_history — журнал начислений/списаний хуиконов (пишется только из add_huikons).
create table public.balance_history (
  id             uuid         primary key default gen_random_uuid(),
  student_id     bigint       references public.students (telegram_id),
  change_amount  integer      not null,
  reason         text         not null,
  created_at     timestamptz  default now()
);

-- homework_submissions — НАСЛЕДИЕ, текущим кодом не используется (см. шапку файла).
create table public.homework_submissions (
  id               uuid         primary key default gen_random_uuid(),
  student_id       bigint       references public.students (telegram_id),
  variant_number   integer      not null,
  photo_url        text         not null,
  status           text         not null default 'pending',
  teacher_comment  text,
  created_at       timestamptz  default now(),
  checked_at       timestamptz
);

-- mock_exam_results — результаты пробников; upsert идемпотентен по (student_id, exam_name).
create table public.mock_exam_results (
  id          uuid         primary key default gen_random_uuid(),
  student_id  bigint       not null references public.students (telegram_id),
  exam_name   text         not null,
  score       text,
  updated_at  timestamptz  not null default now(),
  created_at  timestamptz  not null default now(),
  exam_date   date,
  unique (student_id, exam_name)
);

-- parent_links — привязка родителя к ученику; upsert идемпотентен по (parent_telegram_id, student_id).
create table public.parent_links (
  id                  uuid         primary key default gen_random_uuid(),
  parent_telegram_id  bigint       not null,
  student_id          bigint       not null references public.students (telegram_id),
  linked_at           timestamptz  not null default now(),
  unique (parent_telegram_id, student_id)
);

-- student_payments — платёжные снимки, изолированы от публичного чтения (миграция 002).
-- Без FK на students (историческое решение миграции 002).
create table public.student_payments (
  student_id    bigint  primary key,
  payment_date  date
);

alter table public.student_payments enable row level security;
-- RLS без единой policy = доступ только у service_role (Apps Script);
-- anon-ключ (index.html/teacher.html/боты) платежи не читает (миграция 002).

-- bot_notification_state — персистентная дата последней рассылки main.py (миграция 004).
-- Переживает передеплой/рестарт процесса — без неё "уже отправлено сегодня" терялось при
-- каждом рестарте после времени срабатывания, и рассылка уходила повторно.
create table public.bot_notification_state (
  notification_key  text  primary key,
  last_sent_date    date
);

