-- student_achievements — выданные достижения; строка = достижение получено, она же
-- флаг «право купить косметику» (этап 2). Вехи стрика и дисциплинарные достижения —
-- одно событие с одним начислением (SPEC_STAGE1.md, раздел 6). Идемпотентная выдача:
