-- add_huikons — атомарное начисление/списание хуиконов (миграция 001, задача T5).
CREATE OR REPLACE FUNCTION public.add_huikons(p_student_id bigint, p_amount integer, p_reason text)
 RETURNS TABLE(actual_change integer, new_balance integer)
 LANGUAGE plpgsql
AS $function$
declare
  v_old_balance int;
  v_new_balance int;
begin
  select huikons into v_old_balance
    from students
    where telegram_id = p_student_id
    for update;

  if v_old_balance is null then
    raise exception 'Student % not found', p_student_id;
  end if;

  v_new_balance := greatest(0, v_old_balance + p_amount);

  update students set huikons = v_new_balance where telegram_id = p_student_id;

  insert into balance_history (student_id, change_amount, reason)
    values (p_student_id, v_new_balance - v_old_balance, p_reason);

  return query select (v_new_balance - v_old_balance), v_new_balance;
end;
$function$;

-- get_student_progress — сводка выданных/выполненных заданий по типам (используется родительским ботом).
CREATE OR REPLACE FUNCTION public.get_student_progress(p_student_id bigint)
 RETURNS TABLE(type text, issued bigint, completed bigint)
 LANGUAGE sql
 STABLE
AS $function$
  select
    a.type,
    count(*) filter (where a.activation_status = 'active') as issued,
    count(*) filter (where a.status = 'checked' and a.approval_status = 'approved') as completed
  from assignments a
  where a.student_id = p_student_id
  group by a.type;
$function$;
