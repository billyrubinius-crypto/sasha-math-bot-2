-- --- Таблицы -----------------------------------------------------------------

-- students — родительская таблица для всех внешних ключей.
-- PK = id, но ссылки и вся бизнес-логика идут по telegram_id (см. индекс ниже).
create table public.students (
  id                        bigint  generated by default as identity primary key,
  telegram_id               bigint  not null,
  name                      text,
  rating                    integer default 50,
  huikons                   integer default 0,
  lives                     integer default 3,
  current_streak            integer default 0,
  last_submission_date_msk  date,
  telegram_username         text,
  group_name                text,
  payment_date              date            -- обнулена и не используется (миграция 002)
);

-- Уникальность telegram_id; на этот индекс ссылаются все FK ниже.
create unique index if not exists idx_students_telegram_id
  on public.students (telegram_id);

-- assignments — задание-строка на ученика (ежедневные/еженедельные/индивидуальные).
create table public.assignments (
  id                 uuid         primary key default gen_random_uuid(),
  student_id         bigint       references public.students (telegram_id),
  type               text         not null,
  title              text,
  content_url        text,
  teacher_comment    text,
  day_of_week        integer,
  week_label         text,
  scheduled_date     date,
  activation_status  text         default 'draft',
  status             text         default 'assigned',
  created_at         timestamptz  default now(),
  submitted_at       timestamptz,
  checked_at         timestamptz,
  photo_url          text,
  teacher_feedback   text,
  approval_status    text,
  assigned_group     text,
  -- Поля недельных планов (миграция 011, W01). У legacy-строк все null;
  -- first_submitted_at / revision_* заполняются начиная с W04.
  -- FK plan_item_id -> weekly_plan_items добавляется ниже, после её создания.
  plan_item_id         uuid,
  task_count           integer      check (task_count is null or task_count > 0),
  first_submitted_at   timestamptz,
  revision_deadline_at timestamptz,
  revision_count       integer      check (revision_count is null or revision_count >= 0)
);

-- balance_history — журнал начислений/списаний хуиконов (пишется только из add_huikons).
create table public.balance_history (
