$function$;

-- replace_life_quest — замена (сегодня, до self-report, максимум два раза).
create or replace function public.replace_life_quest(p_student_id bigint)
 returns json
 language plpgsql
as $function$
declare
  v_today date := (now() at time zone 'Europe/Moscow')::date;
  v_id    uuid;
  v_used  integer;
  v_life  text;
  v_excl  text[];
  v_pick  text;
begin
  if not public.stage4_generation_active() then
    raise exception 'Ежедневные квесты ещё не запущены';
  end if;

  select id, replacements_used, life_template_code
    into v_id, v_used, v_life
    from public.student_daily_quests
   where student_id = p_student_id and quest_date = v_today
   for update;

  if not found or v_life is null then
    raise exception 'Сегодняшний жизненный челлендж не сгенерирован';
  end if;
  if exists (select 1 from public.daily_quest_reward_log
              where student_id = p_student_id and quest_date = v_today and reward_kind = 'life') then
    raise exception 'Жизненный челлендж уже подтверждён — замена недоступна';
  end if;
  if v_used >= 2 then
    raise exception 'Достигнут лимит замен на сегодня';
  end if;

  select coalesce(array_agg(template_code), array[]::text[])
    into v_excl
    from public.student_daily_quest_options
   where daily_quest_id = v_id;

  v_pick := public.pick_life_template(v_excl);
  if v_pick is null then
    raise exception 'Нет других доступных челленджей для замены';
  end if;

  update public.student_daily_quests
     set life_template_code = v_pick,
         replacements_used  = v_used + 1,
         updated_at         = now()
   where id = v_id;

  insert into public.student_daily_quest_options (daily_quest_id, template_code, ordinal)
    values (v_id, v_pick, v_used + 1);

  return public.daily_quest_state(p_student_id, v_today);
end;
$function$;

-- grant_life_achievements — pay-zero достижения жизненных привычек (U06, миграция 029, §6/§10.4).
-- Идемпотентный пересчёт из daily_quest_reward_log; выдача через grant_achievement_server(...,0)
-- (без add_huikons/balance_history). Полная мотивация — в 029_stage4_life_achievements.sql.
create or replace function public.grant_life_achievements(p_student_id bigint)
 returns void
 language plpgsql
as $function$
declare
  v_life_count integer;
  v_variety    integer;
  v_max_streak integer;
begin
  select count(*) into v_life_count
    from public.daily_quest_reward_log
   where student_id = p_student_id and reward_kind = 'life';

  if v_life_count >= 1   then perform public.grant_achievement_server(p_student_id, 'life_first', 0); end if;
  if v_life_count >= 7   then perform public.grant_achievement_server(p_student_id, 'life_7',     0); end if;
  if v_life_count >= 30  then perform public.grant_achievement_server(p_student_id, 'life_30',    0); end if;
  if v_life_count >= 100 then perform public.grant_achievement_server(p_student_id, 'life_100',   0); end if;

  select count(distinct q.life_template_code) into v_variety
    from public.daily_quest_reward_log r
    join public.student_daily_quests q
      on q.student_id = r.student_id and q.quest_date = r.quest_date
   where r.student_id = p_student_id and r.reward_kind = 'life'
     and q.life_template_code is not null;

  if v_variety >= 5 then perform public.grant_achievement_server(p_student_id, 'life_variety_5', 0); end if;

  with both_days as (
    select quest_date
      from public.daily_quest_reward_log
     where student_id = p_student_id and reward_kind in ('math', 'life')
     group by quest_date
    having count(distinct reward_kind) = 2
  ),
  grouped as (
    select quest_date - (row_number() over (order by quest_date))::int as grp
      from both_days
  )
  select coalesce(max(cnt), 0) into v_max_streak
    from (select count(*) as cnt from grouped group by grp) runs;

  if v_max_streak >= 7 then perform public.grant_achievement_server(p_student_id, 'life_streak_7', 0); end if;
end;
$function$;

-- claim_life_quest — self-report life=3, идемпотентно (pay-once ledger + add_huikons).
create or replace function public.claim_life_quest(p_student_id bigint)
 returns json
 language plpgsql
as $function$
declare
  v_today date := (now() at time zone 'Europe/Moscow')::date;
  v_id    uuid;
  v_life  text;
  v_paid  integer;
begin
  if not public.stage4_generation_active() then
    raise exception 'Ежедневные квесты ещё не запущены';
  end if;

  select id, life_template_code
    into v_id, v_life
    from public.student_daily_quests
   where student_id = p_student_id and quest_date = v_today
   for update;

  if not found or v_life is null then
    raise exception 'Сегодняшний жизненный челлендж не сгенерирован';
  end if;

  insert into public.daily_quest_reward_log (student_id, quest_date, reward_kind, bubliks)
    values (p_student_id, v_today, 'life', 3)
    on conflict (student_id, quest_date, reward_kind) do nothing;
  get diagnostics v_paid = row_count;
  if v_paid = 1 then
    perform public.add_huikons(p_student_id, 3, 'daily_quest_life');
  end if;

  -- U02C: combo, если math за сегодня уже оплачен (сериализовано FOR UPDATE выше).
  perform public.settle_daily_combo(p_student_id, v_today);

  -- U06: pay-zero достижения жизненных привычек (идемпотентно).
  perform public.grant_life_achievements(p_student_id);

  return public.daily_quest_state(p_student_id, v_today);
end;
$function$;

-- =============================================================================
-- --- Math settlement и combo Stage 4 (миграция 023, карточка U02C; гейт — 024/U02D) --------
-- Принятие сегодняшней/прошлой daily -> pay-once math=3; combo=2 после обоих слотов.
-- Время teacher approval settlement не ограничивает. Гейт — stage4_settlement_active(timestamptz)
-- (точный момент действия vs неизменяемый stage4_started_at, U02D), не generation-флаг. Полная
-- мотивация и rollback — в database/migrations/023_*.sql и 024_stage4_exact_cutover_gate.sql.
-- =============================================================================

-- stage4_settlement_active — гейт выплат (U02D, миграция 024): точное сравнение момента
-- исходного действия с неизменяемым stage4_started_at (timestamptz, без приведения к date).
create or replace function public.stage4_settlement_active(p_action_at timestamptz)
 returns boolean
 language sql
 stable
as $function$
  select ec.stage4_started_at is not null
     and p_action_at is not null
     and p_action_at >= ec.stage4_started_at
    from public.economy_config ec
   where ec.id;
$function$;

-- settle_daily_combo — pay-once combo=2 при наличии math и life за дату (под FOR UPDATE вызова).
create or replace function public.settle_daily_combo(p_student_id bigint, p_quest_date date)
 returns void
 language plpgsql
as $function$
declare
  v_paid integer;
begin
  if exists (select 1 from public.daily_quest_reward_log
              where student_id = p_student_id and quest_date = p_quest_date and reward_kind = 'math')
     and exists (select 1 from public.daily_quest_reward_log
                  where student_id = p_student_id and quest_date = p_quest_date and reward_kind = 'life') then
    insert into public.daily_quest_reward_log (student_id, quest_date, reward_kind, bubliks)
      values (p_student_id, p_quest_date, 'combo', 2)
      on conflict (student_id, quest_date, reward_kind) do nothing;
    get diagnostics v_paid = row_count;
    if v_paid = 1 then
      perform public.add_huikons(p_student_id, 2, 'daily_quest_combo');
    end if;
  end if;
end;
$function$;

-- settle_daily_math — internal math settlement принятой daily (по серверным полям assignment).
create or replace function public.settle_daily_math(p_assignment_id uuid)
 returns void
 language plpgsql
as $function$
declare
  a        public.assignments%rowtype;
  v_qdate  date;
  v_qid    uuid;
  v_target uuid;
  v_paid   integer;
begin
  select * into a from public.assignments where id = p_assignment_id;
  if not found or a.type <> 'daily' then
    return;
  end if;

  if not (a.status = 'checked' and a.approval_status = 'approved'
          and public.is_first_submission_on_time(a.first_submitted_at, a.submitted_at, a.scheduled_date)
          and (coalesce(a.revision_count, 0) = 0
               or (a.revision_deadline_at is not null
                   and a.submitted_at is not null
                   and a.submitted_at <= a.revision_deadline_at))) then
    return;
  end if;

  v_qdate := a.scheduled_date;

  -- U02D: точный timestamp-гейт cutover по моменту ПЕРВОГО действия (не по календарной дате).
  if not public.stage4_settlement_active(coalesce(a.first_submitted_at, a.submitted_at)) then
    return;
  end if;

  -- U07: отсутствующую дневную строку создаём ТОЛЬКО при активной генерации. После rollback
  -- (generation off, stage4_started_at сохранён) settlement уже созданных дней продолжается, но
  -- НОВЫЙ день не создаётся — rollback останавливает новые наборы (SPEC §8).
  if public.stage4_generation_active() then
    perform public.ensure_daily_quest(a.student_id, v_qdate, false);
  end if;

  select id, daily_assignment_id
    into v_qid, v_target
    from public.student_daily_quests
   where student_id = a.student_id and quest_date = v_qdate
   for update;

  if not found then
    return;  -- U07: генерация выключена и дневной строки нет — не создаём и не платим
  end if;

  if v_target is null then
    update public.student_daily_quests
       set daily_assignment_id = a.id, updated_at = now()
     where id = v_qid;
    v_target := a.id;
  end if;

  if v_target <> a.id then
    return;
  end if;

  insert into public.daily_quest_reward_log (student_id, quest_date, reward_kind, bubliks)
    values (a.student_id, v_qdate, 'math', 3)
    on conflict (student_id, quest_date, reward_kind) do nothing;
  get diagnostics v_paid = row_count;
  if v_paid = 1 then
    perform public.add_huikons(a.student_id, 3, 'daily_quest_math');
  end if;

  perform public.settle_daily_combo(a.student_id, v_qdate);

  -- U06: pay-zero достижения жизненных привычек (идемпотентно; поздний math может закрыть streak).
  perform public.grant_life_achievements(a.student_id);
end;
$function$;

