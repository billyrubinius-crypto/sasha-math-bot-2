-- get_student_task_totals — серверный lifetime/range счётчик задач и дней занятий поверх
-- lifecycle-полей W01/W04 (миграция 013, P01A). Read-only производная от assignments: без
-- отдельного изменяемого counter. Принятая работа = status='checked' и
-- approval_status='approved'; solved_tasks суммирует только task_count > 0 (сложение null
-- бессмысленно), active_days считает ВСЕ принятые работы с известной датой отправки, включая
-- legacy с task_count IS NULL (решение пользователя 2026-07-16: день был реально активным
-- независимо от того, известно ли число задач) — такие строки одновременно увеличивают
-- unknown_approved_assignments. p_from/p_to = null → lifetime; иначе диапазон дат МСК
-- включительно, каждая граница независима. Полная мотивация — в шапке миграции 013.
CREATE OR REPLACE FUNCTION public.get_student_task_totals(
  p_student_id bigint,
  p_from       date DEFAULT null,
  p_to         date DEFAULT null
)
 RETURNS TABLE(
   solved_tasks                 bigint,
   active_days                  bigint,
   unknown_approved_assignments bigint
 )
 LANGUAGE sql
 STABLE
AS $function$
  with accepted as (
    select
      a.task_count,
      (coalesce(a.first_submitted_at, a.submitted_at) at time zone 'Europe/Moscow')::date as date_msk
    from public.assignments a
    where a.student_id = p_student_id
      and a.status = 'checked'
      and a.approval_status = 'approved'
      and (p_from is null or (coalesce(a.first_submitted_at, a.submitted_at) at time zone 'Europe/Moscow')::date >= p_from)
      and (p_to   is null or (coalesce(a.first_submitted_at, a.submitted_at) at time zone 'Europe/Moscow')::date <= p_to)
  )
  select
    coalesce(sum(task_count) filter (where task_count > 0), 0)::bigint as solved_tasks,
    count(distinct date_msk)::bigint                                   as active_days,
    count(*) filter (where task_count is null)::bigint                 as unknown_approved_assignments
  from accepted;
$function$;
-- =============================================================================
-- 015_current_week_read_model.sql
-- Read-only current-week model for student/parent consumers (W07 correction).
--
-- W07 duplicated the W04/014 weekly rules in parent_bot.py because the read-only RPC
-- required by SPEC_STAGE2_5 section 10 did not exist. This function restores one server
-- contract: it derives the current MSK week directly from assignments and shield reserves,
-- returns all seven calendar slots plus N/A/S/E and weekly status, and performs no writes.
-- It does not call recalc_student_week and does not create student_week_results.
-- =============================================================================

CREATE OR REPLACE FUNCTION public.get_student_current_week(p_student_id bigint)
 RETURNS jsonb
 LANGUAGE sql
 STABLE
AS $function$
with params as (
  select
    (now() at time zone 'Europe/Moscow')::date as today,
    public.week_start_of((now() at time zone 'Europe/Moscow')::date) as week_start,
    now() as now_ts
),
slots as (
  select p.week_start + gs.day_index as slot_date, gs.day_index
    from params p
    cross join generate_series(0, 6) as gs(day_index)
),
daily_ranked as (
  select
    a.*,
    row_number() over (
      partition by a.scheduled_date
      order by (a.plan_item_id is not null) desc, a.created_at desc, a.id desc
    ) as rn
  from public.assignments a
  cross join params p
  where a.student_id = p_student_id
    and a.type = 'daily'
    and a.scheduled_date between p.week_start and p.week_start + 6
),
daily as (
  select
    a.*,
    public.is_first_submission_on_time(
      a.first_submitted_at, a.submitted_at, a.scheduled_date
    )
    and (
      coalesce(a.revision_count, 0) = 0
      or (
        a.revision_deadline_at is not null
        and a.submitted_at is not null
        and a.submitted_at <= a.revision_deadline_at
      )
    ) as attempt_on_time
  from daily_ranked a
  where a.rn = 1
),
active_shields as (
  select distinct on (u.assignment_id)
    u.assignment_id,
    u.status
  from public.weekly_shield_uses u
  cross join params p
  where u.student_id = p_student_id
    and u.week_start = p.week_start
    and u.status in ('requested', 'consumed')
  order by u.assignment_id,
           case when u.status = 'consumed' then 0 else 1 end,
           u.created_at desc
),
day_rows as (
  select
    s.day_index,
    s.slot_date,
    d.id as assignment_id,
    d.title,
    d.task_count,
    d.revision_deadline_at,
    sh.status as shield_status,
    case
      when d.id is null then 'not_assigned'
      when sh.status is not null then 'shielded'
      when d.status = 'checked' and d.approval_status = 'approved' and d.attempt_on_time
        then 'approved'
      when d.status = 'checked' and d.approval_status = 'rejected'
           and d.revision_deadline_at is not null
           and d.revision_deadline_at > p.now_ts
        then 'revision'
      when d.status = 'submitted' then 'submitted'
      when d.status = 'assigned' and d.scheduled_date < p.today then 'missed'
      when d.status = 'assigned' then 'assigned'
      else 'missed'
    end as day_status
  from slots s
  cross join params p
  left join daily d on d.scheduled_date = s.slot_date
  left join active_shields sh on sh.assignment_id = d.id
),
daily_stats as (
  select
    count(*)::int as n,
    count(*) filter (
      where d.status = 'checked'
        and d.approval_status = 'approved'
        and d.attempt_on_time
    )::int as a,
    coalesce(bool_or(d.status = 'submitted' and d.attempt_on_time), false) as pending_review,
    coalesce(bool_or(
      d.status = 'checked'
      and d.approval_status = 'rejected'
      and d.revision_deadline_at is not null
      and d.revision_deadline_at > p.now_ts
    ), false) as awaiting_student
  from daily d
  cross join params p
),
shield_stats as (
  select count(*)::int as s from active_shields
),
totals as (
  select
    ds.n,
    ds.a,
    ss.s,
    least(ds.n, ds.a + ss.s, 7)::int as e,
    ds.pending_review,
    ds.awaiting_student
  from daily_stats ds
  cross join shield_stats ss
),
classified as (
  select
    t.*,
    case
      when t.pending_review then 'pending_review'
      when t.awaiting_student then 'awaiting_student'
      else 'open'
    end as result_status,
    case
      when t.pending_review or t.awaiting_student then 'pending'
      when t.n < 4 then 'neutral'
      when t.e >= 4 then 'successful'
      else 'weak'
    end as classification
  from totals t
),
weekly_ranked as (
  select
    a.*,
    row_number() over (
      order by (a.plan_item_id is not null) desc, a.created_at desc, a.id desc
    ) as rn
  from public.assignments a
  cross join params p
  where a.student_id = p_student_id
    and a.type = 'weekly'
    and a.week_label = p.week_start::text
),
weekly_payload as (
  select jsonb_build_object(
    'assignment_id', w.id,
    'title', w.title,
    'task_count', w.task_count,
    'status', case
      when w.status = 'assigned' then 'assigned'
      when w.status = 'submitted' then 'submitted'
      when w.status = 'checked' and w.approval_status = 'approved' then 'approved'
      when w.status = 'checked' and w.approval_status = 'rejected' then 'rejected'
      else 'unknown'
    end
  ) as payload
  from weekly_ranked w
  where w.rn = 1
),
days_payload as (
  select jsonb_agg(
    jsonb_build_object(
      'day_index', d.day_index,
      'date', d.slot_date,
      'assignment_id', d.assignment_id,
      'title', d.title,
      'task_count', d.task_count,
      'status', d.day_status,
      'shield_status', d.shield_status,
      'revision_deadline_at', d.revision_deadline_at
    ) order by d.day_index
  ) as payload
  from day_rows d
)
select jsonb_build_object(
  'week_start', p.week_start,
  'week_end', p.week_start + 6,
  'n', c.n,
  'a', c.a,
  's', c.s,
  'e', c.e,
  'result_status', c.result_status,
  'classification', c.classification,
  'reward_forecast', public.weekly_reward_amount(c.e),
  'days', coalesce(dp.payload, '[]'::jsonb),
  'weekly', (select wp.payload from weekly_payload wp limit 1)
)
from params p
cross join classified c
cross join days_payload dp;
$function$;

-- record_weekly_mock_exam — атомарная запись/редактирование результата недельного пробника
-- (миграция 016, P02A; SPEC §9, ECONOMY_V2 §§11,14). Единственная точка записи пробника.
-- База +20 и рекорд +30 бубликов — pay-once через mock_exam_reward_log; season points
-- 50 + рост (cap 20) — детерминированная компенсирующая дельта в weekly_mock_exams.
-- season_points_awarded. Зеркалит display-строку в mock_exam_results для существующих
-- графиков/списков. Блокировка students(for update) сериализует конкурентные вызовы.
-- Полная мотивация и границы — в шапке миграции 016.
CREATE OR REPLACE FUNCTION public.record_weekly_mock_exam(
  p_student_id bigint,
  p_week_start date,
  p_score      integer
)
 RETURNS json
 LANGUAGE plpgsql
AS $function$
declare
  v_prev_score   integer;
  v_prev_max     integer;
  v_growth       integer;
  v_season_target integer;
  v_season_prev  integer;
  v_season_delta integer;
  v_is_record    boolean;
  v_record_this_month boolean;
  v_base_awarded boolean := false;
  v_record_awarded boolean := false;
  v_now          timestamptz := now();
  v_month_start  date := date_trunc('month', (v_now at time zone 'Europe/Moscow'))::date;
  v_exam_name    text;
begin
  if p_week_start is null or extract(isodow from p_week_start) <> 1 then
    raise exception 'week_start % — не понедельник', p_week_start;
  end if;
  if p_score is null or p_score < 0 or p_score > 100 then
    raise exception 'score должен быть целым от 0 до 100';
  end if;

  perform 1 from public.students where telegram_id = p_student_id for update;
  if not found then
    raise exception 'Ученик % не найден', p_student_id;
  end if;

  select score into v_prev_score
    from public.weekly_mock_exams
    where student_id = p_student_id and week_start < p_week_start
    order by week_start desc
    limit 1;

  select max(score) into v_prev_max
    from public.weekly_mock_exams
    where student_id = p_student_id and week_start < p_week_start;

  v_growth := least(greatest(p_score - coalesce(v_prev_score, p_score), 0), 20);
  v_season_target := 50 + v_growth;

  select season_points_awarded into v_season_prev
    from public.weekly_mock_exams
    where student_id = p_student_id and week_start = p_week_start
    for update;

  if not found then
    insert into public.weekly_mock_exams (student_id, week_start, score, season_points_awarded)
      values (p_student_id, p_week_start, p_score, v_season_target);
    v_season_prev := 0;
  else
    update public.weekly_mock_exams
      set score = p_score,
          season_points_awarded = v_season_target,
          updated_at = v_now
      where student_id = p_student_id and week_start = p_week_start;
  end if;

  v_season_delta := v_season_target - v_season_prev;
  if v_season_delta <> 0 then
    perform public.add_season_points(p_student_id, v_season_delta);
  end if;

  v_exam_name := 'Недельный пробник ' || to_char(p_week_start, 'DD.MM.YYYY');
  insert into public.mock_exam_results (student_id, exam_name, score, exam_date, updated_at)
    values (p_student_id, v_exam_name, p_score::text, p_week_start, v_now)
    on conflict (student_id, exam_name)
    do update set score = excluded.score, exam_date = excluded.exam_date, updated_at = v_now;

  insert into public.mock_exam_reward_log (student_id, week_start, reward_kind, bubliks)
    values (p_student_id, p_week_start, 'base', 20)
    on conflict (student_id, week_start, reward_kind) do nothing;
  if found then
    perform public.add_huikons(p_student_id, 20, 'mock_exam_weekly');
    v_base_awarded := true;
  end if;

  v_is_record := v_prev_max is not null and p_score >= v_prev_max + 3;
  if v_is_record then
    select exists (
      select 1 from public.mock_exam_reward_log
        where student_id = p_student_id and reward_kind = 'record'
          and (awarded_at at time zone 'Europe/Moscow')::date >= v_month_start
          and (awarded_at at time zone 'Europe/Moscow')::date < (v_month_start + interval '1 month')
    ) into v_record_this_month;

    if not v_record_this_month then
      insert into public.mock_exam_reward_log (student_id, week_start, reward_kind, bubliks)
        values (p_student_id, p_week_start, 'record', 30)
        on conflict (student_id, week_start, reward_kind) do nothing;
      if found then
        perform public.add_huikons(p_student_id, 30, 'mock_exam_record');
        v_record_awarded := true;
      end if;
    end if;
  end if;

  return json_build_object(
    'week_start', p_week_start,
    'score', p_score,
    'season_points_awarded', v_season_target,
    'season_points_delta', v_season_delta,
    'base_awarded', v_base_awarded,
    'record_eligible', v_is_record,
    'record_awarded', v_record_awarded
  );
end;
$function$;
